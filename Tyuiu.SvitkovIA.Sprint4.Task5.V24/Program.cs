        static int CalculatePositiveCount(int[,] array)
        {
            int positiveCount = 0;
            int rows = array.GetLength(0);
            int columns = array.GetLength(1);

            for (int i = 0; i < rows; i++)
            {
                for (int j = 0; j < columns; j++)
                {
                    if (array[i, j] > 0)
                    {
                        positiveCount++;
                    }
                }
            }

            return positiveCount;
        }
